package math1;

public class Math01P480 {
	public static void main(String[] args) {
		//Math는 자바에서 이미 기본로 제공하는 기능이라
		//import없이 사용이 가능합니다
		
		int a = -1024;
		System.out.println(Math.abs(a));
		
		double b = 3.14;
		System.out.println(Math.ceil(b));
		
		double c = 19.9;
		System.out.println(Math.floor(c));
	}
}
